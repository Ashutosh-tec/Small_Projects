print("Do your calculation")
a = int(input())
o = input()

b = int(input())
if o == "+":
    if a == 56 and b == 9:
        print(77)
    else:
        print(a + b)
elif o == "-":
    print(a - b)
elif o == "*":
    if a == 45 and b == 3:
        print(555)
    else:
        print(a * b)
elif o == "/":
    if a == 56 and b == 6:
        print(4)
    else:
        print(a / b)
